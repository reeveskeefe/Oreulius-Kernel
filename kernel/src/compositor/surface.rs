//! Surface allocator for the compositor.
//!
//! Each window owns a `Surface` that holds its pixel buffer.  Buffers are
//! allocated from the **general kernel page allocator** (`memory::allocate_pages`),
//! not the JIT bump arena, because surfaces are long-lived graphical resources
//! that must be independently reclaimable.
//!
//! ## Format
//! All surfaces use **ARGB8888** (32 bpp, high byte = alpha), matching the
//! legacy compositor pixel convention.

#![allow(dead_code)]

use crate::memory;

/// Bytes per pixel (ARGB8888).
const BPP: usize = 4;

/// A pixel surface: a page-aligned ARGB8888 buffer owned by one window.
pub struct Surface {
    /// Base pointer (page-aligned, from `memory::allocate_pages`).
    ptr: *mut u32,
    /// Width in pixels.
    pub width: u32,
    /// Height in pixels.
    pub height: u32,
    /// Number of 4 KiB pages allocated.
    pages: usize,
    /// Whether this surface slot is live.
    pub alive: bool,
}

// SAFETY: surface memory belongs to one session/window at a time; access is
// serialised by the CompositorService mutex.
unsafe impl Send for Surface {}
unsafe impl Sync for Surface {}

impl Surface {
    /// An empty / uninitialised surface.
    pub const fn empty() -> Self {
        Surface {
            ptr: core::ptr::null_mut(),
            width: 0,
            height: 0,
            pages: 0,
            alive: false,
        }
    }

    /// Allocate a new surface for the given dimensions.
    ///
    /// Returns `None` if the page allocator is exhausted or the dimensions are
    /// zero / exceed the allowed maximum.
    pub fn alloc(width: u32, height: u32) -> Option<Self> {
        if width == 0 || height == 0 {
            return None;
        }
        let pixels = (width as usize).saturating_mul(height as usize);
        let bytes = pixels.saturating_mul(BPP);
        #[cfg(not(target_arch = "aarch64"))]
        let page_size = crate::paging::PAGE_SIZE;
        #[cfg(target_arch = "aarch64")]
        let page_size = 4096usize;
        let pages = (bytes + page_size - 1) / page_size;

        let base = memory::allocate_pages(pages).ok()?;
        let ptr = base as *mut u32;
        // Zero the buffer so the window starts transparent/black.
        unsafe { core::ptr::write_bytes(ptr as *mut u8, 0, pages * page_size) };

        Some(Surface {
            ptr,
            width,
            height,
            pages,
            alive: true,
        })
    }

    /// Free the page allocation and mark the surface dead.
    ///
    /// # Safety
    /// Must not be called while any code holds a reference to the pixel data.
    pub fn free(&mut self) {
        if !self.ptr.is_null() && self.pages > 0 {
            // Zero the buffer (defensive scrub) before returning to the
            // page allocator.  `free_pages` does not exist in the current
            // allocator (bump-style), so we just zero and mark dead; the
            // pages will be reclaimed on process exit via the page table.
            #[cfg(not(target_arch = "aarch64"))]
            let page_size = crate::paging::PAGE_SIZE;
            #[cfg(target_arch = "aarch64")]
            let page_size = 4096usize;
            unsafe {
                core::ptr::write_bytes(self.ptr as *mut u8, 0, self.pages * page_size);
            }
            self.ptr = core::ptr::null_mut();
            self.pages = 0;
        }
        self.alive = false;
    }

    // -----------------------------------------------------------------------
    // Pixel access
    // -----------------------------------------------------------------------

    /// Read one ARGB pixel at `(x, y)`.  Returns 0 for out-of-bounds.
    #[inline]
    pub fn get_pixel(&self, x: u32, y: u32) -> u32 {
        if x >= self.width || y >= self.height || self.ptr.is_null() {
            return 0;
        }
        let offset = (y as usize) * (self.width as usize) + (x as usize);
        unsafe { *self.ptr.add(offset) }
    }

    /// Write one ARGB pixel at `(x, y)`.  No-op for out-of-bounds.
    #[inline]
    pub fn set_pixel(&mut self, x: u32, y: u32, argb: u32) {
        if x >= self.width || y >= self.height || self.ptr.is_null() {
            return;
        }
        let offset = (y as usize) * (self.width as usize) + (x as usize);
        unsafe {
            *self.ptr.add(offset) = argb;
        }
    }

    /// Fill a rectangle with a solid colour.
    pub fn fill_rect(&mut self, x: u32, y: u32, w: u32, h: u32, argb: u32) {
        let x1 = x.min(self.width);
        let y1 = y.min(self.height);
        let x2 = (x.saturating_add(w)).min(self.width);
        let y2 = (y.saturating_add(h)).min(self.height);
        for py in y1..y2 {
            for px in x1..x2 {
                self.set_pixel(px, py, argb);
            }
        }
    }

    /// Draw one row of pixels from a slice of ARGB values.
    pub fn blit_row(&mut self, dst_x: u32, dst_y: u32, src: &[u32]) {
        if dst_y >= self.height {
            return;
        }
        let copy_w = (src.len() as u32).min(self.width.saturating_sub(dst_x));
        for i in 0..copy_w as usize {
            self.set_pixel(dst_x + i as u32, dst_y, src[i]);
        }
    }

    /// Draw text using the built-in 8×8 bitmap font.
    pub fn draw_text(&mut self, x: u32, y: u32, text: &str, fg_argb: u32) -> u32 {
        let mut cx = x;
        let mut drawn = 0u32;
        for ch in text.chars() {
            if cx + 8 > self.width {
                break;
            }
            let glyph = FONT_8X8.get_glyph(ch);
            for row in 0..8u32 {
                if y + row >= self.height {
                    break;
                }
                let bits = glyph[row as usize];
                for col in 0u32..8 {
                    if bits & (0x80 >> col) != 0 {
                        self.set_pixel(cx + col, y + row, fg_argb);
                    }
                }
            }
            cx += 8;
            drawn += 1;
        }
        drawn
    }

    /// Return a raw const pointer to the pixel at (x, y).
    ///
    /// # Safety
    /// Caller must ensure `(x, y)` is in bounds and the surface is alive.
    #[inline]
    pub unsafe fn raw_ptr_at(&self, x: u32, y: u32) -> *const u32 {
        self.ptr
            .add((y as usize) * (self.width as usize) + (x as usize))
    }
}

// ---------------------------------------------------------------------------
// Slot pool
// ---------------------------------------------------------------------------

/// Maximum number of live surfaces.
pub const MAX_SURFACES: usize = 32;

/// Indexed pool of surface slots.  Slot 0 is reserved (means "no surface").
pub struct SurfacePool {
    slots: [Surface; MAX_SURFACES],
}

impl SurfacePool {
    pub const fn new() -> Self {
        const EMPTY: Surface = Surface::empty();
        SurfacePool {
            slots: [EMPTY; MAX_SURFACES],
        }
    }

    /// Allocate a surface and return its slot index, or `None`.
    pub fn alloc(&mut self, width: u32, height: u32) -> Option<usize> {
        for (i, slot) in self.slots.iter_mut().enumerate().skip(1) {
            if !slot.alive {
                *slot = Surface::alloc(width, height)?;
                return Some(i);
            }
        }
        None
    }

    /// Free the surface at `idx`.
    pub fn free(&mut self, idx: usize) {
        if idx > 0 && idx < MAX_SURFACES {
            self.slots[idx].free();
        }
    }

    /// Get an immutable reference.
    pub fn get(&self, idx: usize) -> Option<&Surface> {
        if idx > 0 && idx < MAX_SURFACES && self.slots[idx].alive {
            Some(&self.slots[idx])
        } else {
            None
        }
    }

    /// Get a mutable reference.
    pub fn get_mut(&mut self, idx: usize) -> Option<&mut Surface> {
        if idx > 0 && idx < MAX_SURFACES && self.slots[idx].alive {
            Some(&mut self.slots[idx])
        } else {
            None
        }
    }
}

